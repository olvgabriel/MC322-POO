package game.player;

public class Bag {

	private int items;
	private int value;
	
	
	public Bag() {
		items = 0;
		value = 0;
	}

	public int getItems() {
		return items;
	}

	public void setItems(int items) {
		this.items = items;
	}

	public int getValue() {
		return value;
	}

	public void setValue(int value) {
		this.value = value;
	}
	
	
}
